package com.kimikevin;

public class Expressions {
    public static void main(String[] args) {
        int a = 5, b = 11;
        System.out.println(b + a);
        System.out.println(b - a);
        System.out.println(b * a);
        System.out.println(b / a);
        System.out.println(b % a);
        System.out.println((a - b) + a * (b - a) - a % b);
        System.out.println((-5) * (-10) * (-20));
    }
}
